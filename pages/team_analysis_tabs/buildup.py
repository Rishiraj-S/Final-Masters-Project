def _get_passes(events: pd.DataFrame) -> pd.DataFrame:
    """Extract Barcelona pass rows with valid endpoint coordinates."""
    bar    = events[events['team_code'] == 'BAR']
    passes = bar[
        (bar['event_type'] == 'Pass') &
        bar['x'].notna() & bar['y'].notna()
    ].copy()

    if 'Pass End X' not in passes.columns or 'Pass End Y' not in passes.columns:
        return passes

    passes['Pass End X'] = pd.to_numeric(passes['Pass End X'], errors='coerce')
    passes['Pass End Y'] = pd.to_numeric(passes['Pass End Y'], errors='coerce')
    return passes[passes['Pass End X'].notna() & passes['Pass End Y'].notna()]


def _apply_filters(passes, *, outcomes, start_thirds, end_thirds,
                   bands, players, h1_range, h2_range) -> pd.DataFrame:
    passes = PassMap.filter(
        passes,
        outcomes=outcomes, start_thirds=start_thirds, end_thirds=end_thirds,
        bands=bands, h1_range=h1_range, h2_range=h2_range,
        end_x_col='Pass End X',
    )
    if players and 'player_name' in passes.columns:
        passes = passes[passes['player_name'].isin(players)]
    return passes


