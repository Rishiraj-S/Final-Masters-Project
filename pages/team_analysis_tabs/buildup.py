def _pass_matrix_fig(bar_events: pd.DataFrame) -> go.Figure:
    """Square pass-count heatmap between top-11 Barça players (incl. GK)."""
    ev = bar_events[
        [c for c in ['player_name', 'event_type', 'outcome',
                     'period_id', 'time_min', 'time_sec']
         if c in bar_events.columns]
    ].copy()
    ev = ev.dropna(subset=['player_name'])
    ev = ev.sort_values(['period_id', 'time_min', 'time_sec']).reset_index(drop=True)

    counts: dict = {}
    for i in range(len(ev) - 1):
        row = ev.iloc[i]
        nxt = ev.iloc[i + 1]
        if (row['event_type'] == 'Pass' and row.get('outcome') == 1
                and pd.notna(nxt.get('player_name'))):
            a, b = str(row['player_name']), str(nxt['player_name'])
            if a != b:
                counts[(a, b)] = counts.get((a, b), 0) + 1

    empty_fig = go.Figure()
    empty_fig.update_layout(paper_bgcolor='rgba(0,0,0,0)',
                            plot_bgcolor=PITCH_BG, height=520)
    if not counts:
        return empty_fig

    involvement: dict = {}
    for (a, b), cnt in counts.items():
        involvement[a] = involvement.get(a, 0) + cnt
        involvement[b] = involvement.get(b, 0) + cnt

    top_players = sorted(involvement, key=involvement.get, reverse=True)[:11]

    n         = len(top_players)
    matrix    = np.zeros((n, n))
    total_all = sum(counts.values()) or 1
    pct_matrix = np.zeros((n, n))
    for i, a in enumerate(top_players):
        for j, b in enumerate(top_players):
            if i != j:
                c = counts.get((a, b), 0)
                matrix[i][j]    = c
                pct_matrix[i][j] = c / total_all * 100
    np.fill_diagonal(matrix, np.nan)
    np.fill_diagonal(pct_matrix, np.nan)

    barca_cs = [
        [0.0, '#151932'],
        [0.5, COLORS['primary_blue']],
        [1.0, COLORS['gold']],
    ]

    text_matrix = [
        ['' if i == j else str(int(matrix[i][j])) for j in range(n)]
        for i in range(n)
    ]

    fig = go.Figure(go.Heatmap(
        z=matrix,
        x=top_players,
        y=top_players,
        colorscale=barca_cs,
        showscale=False,
        text=text_matrix,
        texttemplate='%{text}',
        textfont=dict(size=9, color='white'),
        customdata=pct_matrix,
        hovertemplate='<b>%{y} → %{x}</b><br>Passes: %{z:.0f} (%{customdata:.1f}% of team)<extra></extra>',
    ))
    fig.update_layout(
        paper_bgcolor='rgba(0,0,0,0)',
        plot_bgcolor=PITCH_BG,
        font=dict(color=COLORS['text_primary'], size=10, family='Arial, sans-serif'),
        margin=dict(l=40, r=10, t=8, b=40),
        height=520,
        xaxis=dict(
            tickfont=dict(size=8, color=COLORS['text_secondary']),
            showgrid=False,
            constrain='domain',
            title=dict(text='Receiver', font=dict(size=9, color=COLORS['text_secondary'])),
        ),
        yaxis=dict(
            tickfont=dict(size=8, color=COLORS['text_secondary']),
            showgrid=False,
            autorange='reversed',
            scaleanchor='x',
            constrain='domain',
            title=dict(text='Passer', font=dict(size=9, color=COLORS['text_secondary'])),
        ),
        uirevision='buildup-matrix',
    )
    return fig


def _top5_progressive_children(passes: pd.DataFrame) -> list:
    """Top-5 players by progressive-pass count.
    Progressive pass: successful pass where end_x − x ≥ 25 (≥25% of pitch).
    """
    if passes.empty or 'Pass End X' not in passes.columns:
        return [html.P("No data", style={'color': COLORS['text_secondary'], 'fontSize': '0.75rem'})]

    mask_suc  = passes['outcome'].eq(1) if 'outcome' in passes.columns \
                else pd.Series(True, index=passes.index)
    mask_prog = (passes['Pass End X'] - passes['x']) >= 25
    prog = passes[mask_suc & mask_prog]

    if prog.empty or 'player_name' not in prog.columns:
        return [html.P("No data", style={'color': COLORS['text_secondary'], 'fontSize': '0.75rem'})]

    top5 = (
        prog.groupby('player_name').size()
        .sort_values(ascending=False)
        .head(5)
        .reset_index(name='count')
    )

    rows = []
    for rank, row in enumerate(top5.itertuples(), start=1):
        rows.append(html.Div([
            html.Span(f"{rank}", style={
                'color': COLORS['gold'], 'fontWeight': '800',
                'fontSize': '0.85rem', 'minWidth': '18px',
            }),
            html.Span(row.player_name, style={
                'color': 'white', 'fontSize': '0.72rem',
                'flex': '1', 'overflow': 'hidden',
                'textOverflow': 'ellipsis', 'whiteSpace': 'nowrap',
            }),
            html.Span(str(row.count), style={
                'color': COLORS['gold'], 'fontWeight': '700',
                'fontSize': '0.75rem', 'marginLeft': '6px',
            }),
        ], style={
            'display': 'flex', 'alignItems': 'center', 'gap': '8px',
            'padding': '6px 0',
            'borderBottom': f'1px solid {COLORS["dark_border"]}',
        }))

    return [
        html.Div("Top Progressive Passers", style={
            **_SECTION_TITLE,
            'borderBottom': f'1px solid {COLORS["dark_border"]}',
            'marginBottom': '4px',
        }),
        html.Div("(Successful passes advancing ≥ 25% of pitch)", style={
            'color': COLORS['text_secondary'],
            'fontSize': '0.58rem',
            'fontStyle': 'italic',
            'marginBottom': '8px',
        }),
        *rows,
    ]


