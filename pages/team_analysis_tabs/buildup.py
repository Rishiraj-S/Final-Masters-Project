_X_BOUNDS = [0, 100/3, 200/3, 100]
_Y_BOUNDS  = [0, 100/3, 200/3, 100]


def _build_zone_pitch_image(passes: pd.DataFrame, n_total: int) -> str:
    """3×3 zone pitch: gold = % passes sent from zone, white = % received.
    n_total is the denominator (total passes in selected matches, pre-filter).
    """
    if passes.empty:
        return ''

    pitch = Pitch(
        pitch_type='opta', pitch_color=PITCH_BG, line_color=PITCH_LINE_COLOR,
        linewidth=1.5, stripe=False, goal_type='box',
        pad_top=2, pad_bottom=2, pad_left=2, pad_right=2,
    )
    fig_mpl, ax = pitch.draw(figsize=(4.5, 3.0))

    n_total = max(n_total, 1)
    has_end = 'Pass End X' in passes.columns and 'Pass End Y' in passes.columns

    # Pre-compute all sent percentages for scaling cell backgrounds
    sent_grid = {}
    for x0, x1 in zip(_X_BOUNDS[:-1], _X_BOUNDS[1:]):
        for y0, y1 in zip(_Y_BOUNDS[:-1], _Y_BOUNDS[1:]):
            m = ((passes['x'] >= x0) & (passes['x'] < x1) &
                 (passes['y'] >= y0) & (passes['y'] < y1))
            sent_grid[(x0, y0)] = m.sum() / n_total * 100
    max_sent = max(sent_grid.values()) if sent_grid else 1

    for x0, x1 in zip(_X_BOUNDS[:-1], _X_BOUNDS[1:]):
        for y0, y1 in zip(_Y_BOUNDS[:-1], _Y_BOUNDS[1:]):
            sent_pct = sent_grid[(x0, y0)]

            if has_end:
                rm = ((passes['Pass End X'] >= x0) & (passes['Pass End X'] < x1) &
                      (passes['Pass End Y'] >= y0) & (passes['Pass End Y'] < y1))
                recv_pct = rm.sum() / n_total * 100
            else:
                recv_pct = 0.0

            # Heat fill scaled by sent %
            alpha = 0.06 + 0.40 * (sent_pct / max(max_sent, 1))
            ax.add_patch(Rectangle(
                (x0, y0), x1 - x0, y1 - y0,
                facecolor=COLORS['gold'], alpha=alpha, linewidth=0, zorder=1,
            ))

            cx, cy = (x0 + x1) / 2, (y0 + y1) / 2
            ax.text(cx, cy + 5.5, f'S  {sent_pct:.0f}%',
                    ha='center', va='center', fontsize=6.5,
                    color=COLORS['gold'], fontweight='bold', zorder=2)
            ax.text(cx, cy - 5.5, f'R  {recv_pct:.0f}%',
                    ha='center', va='center', fontsize=6.5,
                    color='white', fontweight='bold', zorder=2)

    # Third + band dividers
    for xb in [100/3, 200/3]:
        ax.axvline(xb, color='white', linewidth=0.8, linestyle='--', alpha=0.25, zorder=3)
    for yb in [100/3, 200/3]:
        ax.axhline(yb, color='white', linewidth=0.8, linestyle='--', alpha=0.25, zorder=3)

    fig_mpl.text(0.5, 0.01, 'S = Sent   R = Received',
                 ha='center', va='bottom', fontsize=5.5,
                 color='#aaaaaa', style='italic',
                 transform=fig_mpl.transFigure)

    buf = io.BytesIO()
    fig_mpl.savefig(buf, format='png', dpi=130, bbox_inches='tight',
                    pad_inches=0.05, facecolor=PITCH_BG)
    buf.seek(0)
    result = base64.b64encode(buf.read()).decode()
    plt.close(fig_mpl)
    return result


def _count_key_passes(bar_events: pd.DataFrame) -> int:
    """Count BAR passes immediately followed by a shot in the same match (vectorised)."""
    if bar_events.empty:
        return 0
    sort_cols = [c for c in ['match_id', 'period_id', 'time_min', 'time_sec']
                 if c in bar_events.columns]
    te = bar_events.sort_values(sort_cols).reset_index(drop=True)
    is_shot = te['event_type'].isin(_SHOT_TYPES)
    same_match = (
        (te['match_id'] == te['match_id'].shift(-1))
        if 'match_id' in te.columns
        else pd.Series(True, index=te.index)
    )
    kp = (
        (te['event_type'] == 'Pass') &
        is_shot.shift(-1, fill_value=False) &
        same_match
    )
    return int(kp.sum())


def _stats_numbers_children(passes: pd.DataFrame,
                            bar_events: pd.DataFrame | None = None) -> list:
    """2×2 stat cards — returned as a list for use as div children."""
    n_total = len(passes)
    n_suc   = int(passes['outcome'].eq(1).sum()) if 'outcome' in passes.columns else 0
    acc     = round(n_suc / max(n_total, 1) * 100, 1)

    # Progressive: successful pass advancing ≥25% of pitch length
    if 'Pass End X' in passes.columns and 'x' in passes.columns:
        mask_suc  = passes['outcome'].eq(1) if 'outcome' in passes.columns \
                    else pd.Series(True, index=passes.index)
        n_prog = int((mask_suc & ((passes['Pass End X'] - passes['x']) >= 25)).sum())
    else:
        n_prog = 0

    # Key passes: pass immediately followed by a shot in the same match
    if bar_events is not None and not bar_events.empty:
        n_key = _count_key_passes(bar_events)
    else:
        n_key = int(pd.to_numeric(passes['Key Pass'], errors='coerce').eq(1).sum()) \
                if 'Key Pass' in passes.columns else 0

    def _card(value, label, color):
        return html.Div([
            html.Div(str(value), style={
                'color': color, 'fontWeight': '800',
                'fontSize': '1.5rem', 'lineHeight': '1.1',
            }),
            html.Div(label, style={
                'color': COLORS['text_secondary'],
                'fontSize': '0.63rem', 'fontWeight': '600',
                'letterSpacing': '0.6px', 'textTransform': 'uppercase',
                'marginTop': '3px',
            }),
        ], style={
            'backgroundColor': COLORS['dark_secondary'],
            'border': f'1px solid {COLORS["dark_border"]}',
            'borderRadius': '6px',
            'padding': '10px 12px',
            'flex': '1',
        })

    def _row(*cards):
        return html.Div(list(cards), style={'display': 'flex', 'gap': '6px', 'marginBottom': '6px'})

    return [
        _row(_card(n_total,   'Total',       GOLD),
             _card(f'{acc}%', 'Accuracy',    HOME_COLOR)),
        _row(_card(n_prog,    'Progressive', HOME_COLOR),
             _card(n_key,     'Key Passes',  AWAY_COLOR)),
    ]


