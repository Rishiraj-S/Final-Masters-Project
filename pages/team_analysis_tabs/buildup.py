def _filter_touches(bar_events: pd.DataFrame, *,
                    players, h1_range, h2_range) -> pd.DataFrame:
    """Return BAR events filtered by player and half-time ranges."""
    df = bar_events.copy()
    if players and 'player_name' in df.columns:
        df = df[df['player_name'].isin(players)]
    if 'period_id' in df.columns and 'time_min' in df.columns:
        h1_lo, h1_hi = h1_range
        h2_lo, h2_hi = h2_range
        m1 = (df['period_id'] == 1) & (df['time_min'] >= h1_lo) & (df['time_min'] <= h1_hi)
        m2 = (df['period_id'] == 2) & (df['time_min'] >= h2_lo) & (df['time_min'] <= h2_hi)
        df = df[m1 | m2]
    return df.dropna(subset=['x', 'y'])


