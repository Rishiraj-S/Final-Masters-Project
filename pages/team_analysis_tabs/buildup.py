def _network_fig_bar(nodes: pd.DataFrame, edges: dict) -> go.Figure:
    """Plotly pass-network figure using buildup pitch background."""
    fig = go.Figure()
    _add_pitch_background(fig)

    if edges:
        max_cnt = max(edges.values())
        for (a, b), cnt in edges.items():
            an = nodes[nodes['player_name'] == a]
            bn = nodes[nodes['player_name'] == b]
            if an.empty or bn.empty:
                continue
            x0, y0 = float(an.iloc[0]['x']), float(an.iloc[0]['y'])
            x1, y1 = float(bn.iloc[0]['x']), float(bn.iloc[0]['y'])
            width = 1.0 + (cnt / max_cnt) * 5.0

            if (b, a) in edges:
                dx, dy  = x1 - x0, y1 - y0
                length  = (dx**2 + dy**2) ** 0.5 or 1.0
                ox, oy  = -dy / length * 2.5, dx / length * 2.5
                xm, ym  = (x0 + x1) / 2 + ox, (y0 + y1) / 2 + oy
                t       = np.linspace(0, 1, 25)
                cx_pts  = (1-t)**2 * x0 + 2*(1-t)*t * xm + t**2 * x1
                cy_pts  = (1-t)**2 * y0 + 2*(1-t)*t * ym + t**2 * y1
            else:
                cx_pts, cy_pts = [x0, x1], [y0, y1]
                xm, ym = (x0 + x1) / 2, (y0 + y1) / 2

            fig.add_trace(go.Scatter(
                x=cx_pts, y=cy_pts, mode='lines',
                line=dict(color=COLORS['primary_blue'], width=width),
                opacity=0.55, hoverinfo='skip', showlegend=False,
            ))
            fig.add_trace(go.Scatter(
                x=[xm], y=[ym], mode='markers',
                marker=dict(size=14, color='rgba(0,0,0,0)', opacity=0),
                customdata=[[str(a), str(b), cnt]],
                hovertemplate=(
                    '<b>%{customdata[0]} → %{customdata[1]}</b><br>'
                    'Passes: %{customdata[2]}<extra></extra>'
                ),
                showlegend=False,
            ))

    if not nodes.empty:
        fig.add_trace(go.Scatter(
            x=nodes['x'], y=nodes['y'],
            mode='markers+text',
            marker=dict(size=nodes['size'], color=COLORS['primary_blue'],
                        line=dict(width=2, color='white')),
            text=nodes['label'],
            textposition='middle center',
            textfont=dict(size=8, color='white', family='Arial Black'),
            customdata=nodes[['player_name', 'involvement']].values,
            hovertemplate=(
                '<b>%{customdata[0]}</b><br>'
                'Involvement: %{customdata[1]:.0f} connections<extra></extra>'
            ),
            showlegend=False,
        ))

    _add_attack_direction(fig)
    fig.update_layout(
        **PITCH_AXIS_FULL,
        paper_bgcolor='rgba(0,0,0,0)',
        plot_bgcolor='rgba(0,0,0,0)',
        font=dict(color='#E8E9ED', size=12, family='Arial, sans-serif'),
        margin=dict(l=0, r=0, t=36, b=0),
        showlegend=False,
        height=520,
        uirevision='buildup-network',
        hovermode='closest',
    )
    return fig


def _pass_matrix_fig(bar_events: pd.DataFrame, pass_pairs: dict | None = None) -> go.Figure:
    """Square pass-count heatmap between top-11 Barça players (incl. GK).

    pass_pairs: pre-computed {(passer, receiver): count} from _count_pass_pairs.
    If None, the pairs are computed here (single-call path).
    """
    counts = pass_pairs if pass_pairs is not None else _count_pass_pairs(bar_events)

    empty_fig = go.Figure()
    empty_fig.update_layout(paper_bgcolor='rgba(0,0,0,0)',
                            plot_bgcolor=PITCH_BG, height=520)
    if not counts:
        return empty_fig

    involvement: dict = {}
    for (a, b), cnt in counts.items():
        involvement[a] = involvement.get(a, 0) + cnt
        involvement[b] = involvement.get(b, 0) + cnt

    top_players = sorted(involvement, key=involvement.get, reverse=True)[:11]

    n         = len(top_players)
    matrix    = np.zeros((n, n))
    total_all = sum(counts.values()) or 1
    pct_matrix = np.zeros((n, n))
    for i, a in enumerate(top_players):
        for j, b in enumerate(top_players):
            if i != j:
                c = counts.get((a, b), 0)
                matrix[i][j]    = c
                pct_matrix[i][j] = c / total_all * 100
    np.fill_diagonal(matrix, np.nan)
    np.fill_diagonal(pct_matrix, np.nan)

    barca_cs = [
        [0.0, '#151932'],
        [0.5, COLORS['primary_blue']],
        [1.0, COLORS['gold']],
    ]

    text_matrix = [
        ['' if i == j else str(int(matrix[i][j])) for j in range(n)]
        for i in range(n)
    ]

    fig = go.Figure(go.Heatmap(
        z=matrix,
        x=top_players,
        y=top_players,
        colorscale=barca_cs,
        showscale=False,
        text=text_matrix,
        texttemplate='%{text}',
        textfont=dict(size=9, color='white'),
        customdata=pct_matrix,
        hovertemplate='<b>%{y} → %{x}</b><br>Passes: %{z:.0f} (%{customdata:.1f}% of team)<extra></extra>',
    ))
    fig.update_layout(
        paper_bgcolor='rgba(0,0,0,0)',
        plot_bgcolor=PITCH_BG,
        font=dict(color=COLORS['text_primary'], size=10, family='Arial, sans-serif'),
        margin=dict(l=40, r=10, t=8, b=40),
        height=520,
        xaxis=dict(
            tickfont=dict(size=8, color=COLORS['text_secondary']),
            showgrid=False,
            constrain='domain',
            title=dict(text='Receiver', font=dict(size=9, color=COLORS['text_secondary'])),
        ),
        yaxis=dict(
            tickfont=dict(size=8, color=COLORS['text_secondary']),
            showgrid=False,
            autorange='reversed',
            scaleanchor='x',
            constrain='domain',
            title=dict(text='Passer', font=dict(size=9, color=COLORS['text_secondary'])),
        ),
        uirevision='buildup-matrix',
    )
    return fig


